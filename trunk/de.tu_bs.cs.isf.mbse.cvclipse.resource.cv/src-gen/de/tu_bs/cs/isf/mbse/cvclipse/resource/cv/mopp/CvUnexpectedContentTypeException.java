package de.tu_bs.cs.isf.mbse.cvclipse.resource.cv.mopp;

/**
 * An Excpetion to represent invalid content types for parser instances.
 * 
 * @see de.tu_bs.cs.isf.mbse.cvclipse.resource.cv.ICvOptions.RESOURCE_CONTENT_TYPE
 */
public class CvUnexpectedContentTypeException extends org.antlr.runtime3_4_0.RecognitionException {
	
	private static final long serialVersionUID = 4791359811519433999L;
	
	private Object contentType = null;
	
	public  CvUnexpectedContentTypeException(Object contentType) {
		this.contentType = contentType;
	}
	
	public Object getContentType() {
		return contentType;
	}
	
}
